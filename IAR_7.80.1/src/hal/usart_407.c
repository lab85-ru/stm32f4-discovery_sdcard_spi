#include "stm32f407xx.h"

//------------------------------------------------------------------------------
// usart - reset (disable)
//------------------------------------------------------------------------------
void usart_reset(USART_TypeDef *u)
{
    u->CR1 = 0;  // clear
    u->CR2 = 0;  // clear
    u->CR3 = 0;  // clear
    u->BRR = 0;  // clear
}
